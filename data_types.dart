void main() {
  //Data Types

  //Strings
  String firstName = "Destiny";
  String lastName = "Saturday";
  print("My first name is $firstName $lastName,");

  //Numbers

  //Integers
  int age = 19;
  print("I am $age years old.");

  //Doubles
  double height = 5.11;
  print("I am $height feet tall.");

  //Booleans
  bool isTall = true;
  print("\nQuestion: \nIs $firstName tall? \nAnswer: $isTall");
}
